# This code requests user input for their name, age and address and prints all these details on one line
name = input("What is your name:")
age = input("What is your age:")
house_number = input("What is your house number:")
street_name = input("What is the name of your street:")
print(f"This is {name}, he is {age} years old, and lives at {house_number}, {street_name}")